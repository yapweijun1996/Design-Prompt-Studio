// ─── Lookup helpers ─────────────────────────────────────────────────────────
export const LIBRARY_BY_ID = Object.fromEntries(LIBRARIES.map((l) => [l.id, l]));

export function getLibrary(id) {
  return LIBRARY_BY_ID[id] || null;
}

export function getLibrariesByCategory(catId) {
  return LIBRARIES.filter((l) => l.category === catId);
}

export function getLibrariesByStack(stack) {
  return LIBRARIES.filter((l) => l.stacks.includes(stack));
}

export function libraryCount() {
  return LIBRARIES.length;
}
